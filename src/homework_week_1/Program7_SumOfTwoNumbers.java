/**
 * 7. Write a program to print the sum of two numbers
 * Test Data : 74 + 36
 * Expected Output : 110
 */
public class Program7_SumOfTwoNumbers {
    public static void main(String[] args) {
        //The two integer are value1 and value 2
        int value1 = 74;
        int value2 = 36;
        int sum = value1 + value2;
        //Concatenation
        System.out.println("The sum of "+value1+" and "+ value2 +" is "+sum);
    }
}
